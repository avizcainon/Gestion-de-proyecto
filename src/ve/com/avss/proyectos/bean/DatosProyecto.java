package ve.com.avss.proyectos.bean;

public class DatosProyecto {
	private String idBD = "";
	private String descripcion = "";
	private String fechaDesarrollo = "";
	private String fechaCertificacion = "";
	private String observacion = "";
	private String status = "";

	
	
	public String getIdBD() {
		return idBD;
	}
	public void setIdBD(String idBD) {
		this.idBD = idBD;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getFechaDesarrollo() {
		return fechaDesarrollo;
	}
	public void setFechaDesarrollo(String fechaDesarrollo) {
		this.fechaDesarrollo = fechaDesarrollo;
	}
	public String getFechaCertificacion() {
		return fechaCertificacion;
	}
	public void setFechaCertificacion(String fechaCertificacion) {
		this.fechaCertificacion = fechaCertificacion;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
