package ve.com.avss.proyectos.bean;

public class DatosTarea {
	private String idBD = "";
	private String descripcion = "";
	private String fechaDesarrollo = "";
	private String observacion = "";
	private String status = "";
	private String recurso = "";
	private String proyecto = "";

	
	
	public String getIdBD() {
		return idBD;
	}
	public void setIdBD(String idBD) {
		this.idBD = idBD;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getFechaDesarrollo() {
		return fechaDesarrollo;
	}
	public void setFechaDesarrollo(String fechaDesarrollo) {
		this.fechaDesarrollo = fechaDesarrollo;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRecurso() {
		return recurso;
	}
	public void setRecurso(String recurso) {
		this.recurso = recurso;
	}
	public String getProyecto() {
		return proyecto;
	}
	public void setProyecto(String proyecto) {
		this.proyecto = proyecto;
	}
	
	

}
